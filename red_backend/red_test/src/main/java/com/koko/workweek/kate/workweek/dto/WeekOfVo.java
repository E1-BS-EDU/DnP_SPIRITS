package com.koko.workweek.kate.workweek.dto;

public class WeekOfVo {
	private String dt;
	private String sdt;
	private String edt;
	private String nextSdt;
	private String nextEdt;
	private String ym;
	private String weekOf;
	
	public String getDt() {
		return dt;
	}
	public void setDt(String dt) {
		this.dt = dt;
	}
	public String getSdt() {
		return sdt;
	}
	public void setSdt(String sdt) {
		this.sdt = sdt;
	}
	public String getEdt() {
		return edt;
	}
	public void setEdt(String edt) {
		this.edt = edt;
	}
	public String getNextSdt() {
		return nextSdt;
	}
	public void setNextSdt(String nextSdt) {
		this.nextSdt = nextSdt;
	}
	public String getNextEdt() {
		return nextEdt;
	}
	public void setNextEdt(String nextEdt) {
		this.nextEdt = nextEdt;
	}
	public String getYm() {
		return ym;
	}
	public void setYm(String ym) {
		this.ym = ym;
	}
	public String getWeekOf() {
		return weekOf;
	}
	public void setWeekOf(String weekOf) {
		this.weekOf = weekOf;
	}	
}